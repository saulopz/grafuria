class State:
    """
    Enumeration to represents Edge, Vertex and Node states.

    Attributes
    ----------
    NONE : int
        Represents an element without state.
    TESTING : int
        Represents an element that is being tested.
    ACTIVE : int
        Represents an element activated.
    INVALID : int
        Represents an invalid element.
    """

    NONE = 0
    TESTING = 1
    ACTIVE = 2
    INVALID = 3
